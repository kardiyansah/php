// Abstract Class
// Sebuah class yang tidak dapat di-instance
// Mendefinisikan Interface untuk kelas yang menjadi turunanya
// Berperan sebagai 'kerangka dasar' untuk kelas turunanya
// Harus memiliki minimal 1 method abstract ( interface )
// Digunakan dalam pewarisan / inheritance untuk memaksakan implementasi method abstract yang sama untuk semua kelas turunannya
// Semua class turunannya harus mengimplementasikan method abstract yang ada di kelas abstract
// Class abstract boleh memiliki property / method reguler
// Class abstract boleh memiliki property / static method